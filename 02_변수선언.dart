main() {
  // 변수 선언 
  // var : variable : 데이터에 의한 추론형 변수 선언
  
  var name = '장비'; // string
  // name = '유비';
  print(name);

  var height = 170; // int
  print(height);

  var weight = 48.8; // float, double 실수형
  print(weight);

  var gender = true; // 부울 타입 (Boolean / true , false)
  print(gender);

  print("내 친구의 이름은 " + name + "입니다.");
}